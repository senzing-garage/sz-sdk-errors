#! /usr/bin/env python3

"""
Used to generate python/szerror.py
"""

import json
import logging
import os
from datetime import datetime, timezone

INPUT_FILE = "szerrors.json"
OUTPUT_FILE = "java/SzExceptionMapper.java"
PAD_CLASS = 35


def spaces_not_tabs():
    """Because tabs are used in OUTPUT_HEADER, linters get confused with spaces vs. tabs.  This solves it."""


# -----------------------------------------------------------------------------
# --- Main
# -----------------------------------------------------------------------------

# Set up logging.

logging.basicConfig(format="%(asctime)s %(message)s", level=logging.INFO)

logging.info("-" * 80)
logging.info("--- %s - Begin", os.path.basename(__file__))
logging.info("-" * 80)

# Create multi-line strings for output.

OUTPUT_HEADER = """/**
DO NOT EDIT.  This code is generated.
Generated by: sz-sdk-errors/bin/generate_java.py
Generated for: sz-sdk-java/src/main/java/com/senzing/sdk/core/SzExceptionMapper.java
"""

OUTPUT_HEADER += f"Generated date: {datetime.now(timezone.utc).isoformat()}\n"
OUTPUT_HEADER += """ */

package com.senzing.sdk.core;

import java.util.Map;
import com.senzing.sdk.*;

/**
 * Package-access class for mapping Senzing error code to
 * instances of {@link SzException}.
 */
final class SzExceptionMapper {
    /**
     * Private constructor since all methods are static.
     */
    private SzExceptionMapper() {
        // do nothing
    }

    /**
     * Obtains the exception class for the specified error code.
     *
     * @param map The {@link Map} to which to add the mappings of
     *            error code keys and exception class objects.
     */
    public static void registerExceptions(Map<Integer, Class<? extends SzException>> map) {
"""


OUTPUT_FOOTER = """    }
}
"""

with open(INPUT_FILE, encoding="utf-8") as input_file:
    errors = json.load(input_file)

with open(OUTPUT_FILE, "w", encoding="utf-8") as file:
    file.write(OUTPUT_HEADER)
    for error_number, error_data in errors.items():
        OUTPUT_LINE = ""
        error_class = error_data.get("class")
        if error_class:
            OUTPUT_LINE = f"        map.put({error_number}, {error_class}.class);"
            error_name = error_data.get("name")
            error_comment = error_data.get("comment")
            if error_name or error_comment:
                pad_class_len = len(error_number) + len(error_class)
                OUTPUT_LINE += (
                    " " * (PAD_CLASS - pad_class_len)
                ) + f'  // {error_name:<85} "{error_comment}"'
        if len(OUTPUT_LINE) > 0:
            OUTPUT_LINE += "\n"
            file.write(OUTPUT_LINE)
    file.write(OUTPUT_FOOTER)

# Epilog.

logging.info("-" * 80)
logging.info("--- %s - End", os.path.basename(__file__))
logging.info("-" * 80)
